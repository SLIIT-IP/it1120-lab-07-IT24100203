public class IT24100203Lab7Q2C {
   public static void main (String[]args) {
	   // Outer loop to handle the number of lines (starting from 5)
	   for (int i=5; i>=1; i--) {
		   for (int j=1; j<=i; j++) {
			   System.out.print(i); //to print the current line
		   }
		   System.out.println("");
	   }
   }
}
